#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	while (*s)
	{
		if ((char)c == *s)
			return ((char *)s);
		++s;
	}
	if ((char)c == '\0' && *s == '\0')
		return ((char *)s);
	else
		return (NULL);
}
