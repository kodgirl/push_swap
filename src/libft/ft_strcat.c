#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int m;

	i = 0;
	m = 0;
	while (s1[i] != '\0')
		++i;
	while (s2[m] != '\0')
	{
		s1[i] = s2[m];
		++i;
		++m;
	}
	s1[i] = '\0';
	return (s1);
}
